function translatePage(lang) {
    currentLanguage = lang;
    localStorage.setItem('feelynxLang', lang);
    translateInterface();
}

function translateInterface() {
    const currentTranslations = translations[currentLanguage];
    if (!currentTranslations) return;
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = currentTranslations[key];
        if (translation) {
            if (element.hasAttribute('placeholder')) {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        }
    });
}

// Animate Statistics Counters
function animateStatCounters() {
    const statValues = document.querySelectorAll('.stat-value');
    statValues.forEach(stat => {
        const countData = stat.dataset.count;
        if (countData) {
            const target = parseInt(countData);
            animateCounter(stat, target);
        }
    });
}

function animateCounter(element, target) {
    let current = 0;
    const increment = target / 100;
    const timer = setInterval(() => {
        current += increment;
        if (current >= target) {
            current = target;
            clearInterval(timer);
        }
        
        if (target >= 1000000) {
            element.textContent = '$' + (current / 1000000).toFixed(1) + 'M+';
        } else if (target >= 1000) {
            element.textContent = (current / 1000).toFixed(0) + 'k+';
        } else {
            element.textContent = Math.floor(current).toLocaleString() + '+';
        }
    }, 20);
}

// Enhanced Navigation System
function initializeNavigation() {
    const navTabs = document.querySelectorAll('.nav-tab');
    const tabContents = document.querySelectorAll('.tab-content');
    const sectionTabs = document.querySelectorAll('.section-tab');
    
    navTabs.forEach(tab => {
        tab.addEventListener('click', function() {
            const targetTab = this.dataset.tab;
            
            // Animated tab transition
            navTabs.forEach(t => {t.classList.remove('active');t.setAttribute('aria-selected','false');t.setAttribute('tabindex','-1');});
            this.classList.add('active');this.setAttribute('aria-selected','true');this.setAttribute('tabindex','0');
            
            tabContents.forEach(content => {
                content.classList.remove('active');
                if (content.id === targetTab) {
                    setTimeout(() => content.classList.add('active'), 100);
                }
            });
            
            currentTab = targetTab;
            
            // Load tab-specific content
            loadTabContent(targetTab);
        });
    });
    
    // Section navigation for Explore tab
    sectionTabs.forEach(tab => {
        tab.addEventListener('click', function() {
            const targetSection = this.dataset.section;
            
            sectionTabs.forEach(t => t.classList.remove('active'));
            this.classList.add('active');this.setAttribute('aria-selected','true');this.setAttribute('tabindex','0');
            
            document.querySelectorAll('.creator-section').forEach(section => {
                section.classList.remove('active');
                if (section.id === targetSection) {
                    section.classList.add('active');
                }
            });
            
            currentSection = targetSection;
            loadSectionContent(targetSection);
        });
    });
}

